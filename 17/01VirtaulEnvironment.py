# commands to create Virtual environment 
'''
step 1: 
    Install Virtual Environment using the Command: pip install virtualenv
Step 2:
    Verify your installation using Command:virtualenv --version
Step 3:
    Create a virtualenv using the following command: virtualenv my_name
Step 4:
     Activate Virtualenv Using the Following Commnad:
        Linux/MacOs Users: source virtualenv_name/bin/activate
        Windows Users: 
                    Using Powershell: <virtualenv_name>\Scripts\Activate.ps1
                    Using CMD: <virtualenv_name>\Scripts\Activate.bat

'''
